const mongoose = require("mongoose");

const UserDetailsScehma = new mongoose.Schema(
  {
    fname: String,
    lname: String,
    email: { type: String, unique: true },
    password: String,
    userType: String,
  },
  {
    collection: "UserInfo",
  }
);

const UserDetailsModel = mongoose.model("UserInfo", UserDetailsScehma);

export { UserDetailsModel as UserInfo };